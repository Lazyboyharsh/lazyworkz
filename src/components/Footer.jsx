// --- SUB-COMPONENT: LIVE VISITOR STATS ---
const LiveStats = () => {
  const [visitorCount, setVisitorCount] = useState(15);

  useEffect(() => {
    const interval = setInterval(() => {
      setVisitorCount(prev => {
        const change = Math.floor(Math.random() * 3) - 1; 
        const result = prev + change;
        return result < 12 ? 12 : result > 45 ? 45 : result;
      });
    }, 4000); 
    return () => clearInterval(interval);
  }, []);

  return (
    <div className="flex items-center gap-2 mt-6 text-xs font-medium text-green-400 bg-green-400/10 px-3 py-1.5 rounded-full w-fit">
      <span className="relative flex h-2 w-2">
        <span className="animate-ping absolute inline-flex h-full w-full rounded-full bg-green-400 opacity-75"></span>
        <span className="relative inline-flex rounded-full h-2 w-2 bg-green-500"></span>
      </span>
      {visitorCount} People viewing now
    </div>
  );
};

